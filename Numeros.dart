void main() {
  //Tipos de variable

  //Se usa la palabra "INT" antes de la variable para indicar que su valor sera de tipo entero
  int age = 20;
  int weeks = 4;
  int year = 2022;

  //Se usa la palabra "DOUBLE" antes de la variable para indicar que su valor sera de tipo flotante o decimal

  double rating = 4.5;
  double grade = 4.9;

  print(age);
  print(weeks);
  print(year);
  print(rating);
  print(grade);
}
